function q = guess_q_format(fname, ctype, dims)
    % Guess Q format from filename pattern and data type
    if contains(fname, 'erb_')
        q = 'Q15';  % u16f15
    elseif contains(fname, '_bias') && ~contains(fname, '_running_')
        if contains(fname, 'gru_')
            q = 'Q10';  % s16f10 (GRU bias)
        elseif contains(fname, 'fc_')
            q = 'Q20';  % s32f20
        elseif contains(fname, 'affine_')
            q = 'Q20';  % s32f20
        else
            % Default bias
            if contains(ctype, '32')
                q = 'Q20';
            else
                q = 'Q13';
            end
        end
    elseif contains(fname, '_running_mean')
        q = 'Q20';  % s32f20
    elseif contains(fname, '_running_var')
        q = 'Q11-Q14';  % u16f11-u16f14 (varies by block)
    elseif contains(fname, '_slope')
        q = 'Q13';  % s16f13
    elseif contains(fname, '_weight')
        if contains(fname, 'gru_')
            q = 'Q12';  % s16f12
        elseif contains(fname, 'fc_')
            q = 'Q13';  % s16f13
        elseif contains(fname, 'ln_')
            q = 'Q12';  % s16f12
        elseif contains(fname, 'affine_')
            q = 'Q14';  % s16f14
        elseif contains(fname, 'bn_') || contains(fname, 'ops_2_')
            q = 'Q14';  % s16f14/u16f14
        else
            % Conv/PConv/GConv/TConv weights
            q = 'Q13/Q14';
        end
    else
        q = 'UNKNOWN';
    end
end

