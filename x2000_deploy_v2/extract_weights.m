% extract_weights.m — Export all pre-quantized weights from para_in_mat_FP/ to C arrays
% Output: ulunas_matlab_weights.h + ulunas_matlab_weights.c + qr_config.h + layer_dims.h
% All weights are ALREADY quantized — this script just copies them to C format.

clear; clc;

fp_dir   = fullfile(fileparts(mfilename('fullpath')), '..', 'para_in_mat_FP');
float_dir = fullfile(fileparts(mfilename('fullpath')), '..', 'para_in_mat');
out_dir  = fileparts(mfilename('fullpath'));

addpath(fp_dir);
addpath(float_dir);

%% ====================================================================== %
%% 1. Scan all .mat files
%% ====================================================================== %
files = dir(fullfile(fp_dir, '*.mat'));
fprintf('Found %d .mat weight files\n', length(files));

% Build a struct map: key = C variable name, value = {data, type_str, dims, comment}
weight_map = containers.Map();

for i = 1:length(files)
    fname = files(i).name;
    [~, basename, ~] = fileparts(fname);

    data_struct = load(fullfile(fp_dir, fname));
    var_names = fieldnames(data_struct);
    data = data_struct.(var_names{1});

    % Determine C type from MATLAB class
    if isa(data, 'double') || isa(data, 'single')
        % Double/single — may need to determine signedness
        if all(data(:) >= 0) && max(data(:)) <= 65535
            if max(data(:)) <= 32767
                ctype = 'int16_t';
                mltype = 'int16';
            else
                ctype = 'uint16_t';
                mltype = 'uint16';
            end
        elseif all(data(:) >= -32768 & data(:) <= 32767)
            ctype = 'int16_t';
            mltype = 'int16';
        elseif all(data(:) >= 0 & data(:) <= 4294967295)
            if max(data(:)) <= 2147483647
                ctype = 'int32_t';
                mltype = 'int32';
            else
                ctype = 'uint32_t';
                mltype = 'uint32';
            end
        else
            ctype = 'int32_t';
            mltype = 'int32';
        end
        % Override: running_var always non-negative → force uint16_t
        if contains(basename, '_running_var')
            ctype = 'uint16_t';
            mltype = 'uint16';
        end
        % Convert to integer using MATLAB class name
        data = cast(data, mltype);
    elseif isinteger(data)
        % Already an integer — map MATLAB class to C type
        mltype = class(data);
        switch mltype
            case 'int16'
                ctype = 'int16_t';
            case 'uint16'
                ctype = 'uint16_t';
            case 'int32'
                ctype = 'int32_t';
            case 'uint32'
                ctype = 'uint32_t';
            case 'int64'
                ctype = 'int64_t';
            case 'uint64'
                ctype = 'uint64_t';
            case 'int8'
                ctype = 'int8_t';
            case 'uint8'
                ctype = 'uint8_t';
            otherwise
                error('Unsupported integer type for %s: %s', fname, mltype);
        end
    else
        error('Unknown type for %s: %s', fname, class(data));
    end

    % Create C-safe variable name
    c_name = strrep(strrep(strrep(basename, '.', '_'), '-', '_'), ' ', '_');

    % Determine dims for comment
    dims = size(data);
    dim_str = sprintf('[%s]', strjoin(arrayfun(@num2str, dims, 'UniformOutput', false), '×'));

    % Determine Q format comment based on filename pattern
    q_comment = guess_q_format(basename, ctype, dims);

    % Plan A: GRU HH/IH weights — reload from float, quantize to int32 Q20
    is_gru_w = contains(basename, '_gru_weight_ih_') || contains(basename, '_gru_weight_hh_');
    if is_gru_w
        float_file = fullfile(float_dir, fname);
        if exist(float_file, 'file')
            float_data = load(float_file);
            float_vars = fieldnames(float_data);
            float_vals = double(float_data.(float_vars{1}));
            data = int32(round(float_vals * 1048576));
            ctype = 'int32_t';
            q_comment = 'Q20 (upgraded from Q12)';
        end
    end

    weight_map(c_name) = struct('data', data, 'ctype', ctype, 'dims', dim_str, 'q_comment', q_comment);
end

fprintf('Processed %d weight entries\n', weight_map.Count);

