    k = keys_list{i};
    w = weight_map(k);
    total = numel(w.data);

    fprintf(fid_c, '/* %s %s %s */\n', w.q_comment, w.ctype, w.dims);
    fprintf(fid_c, 'const %s %s[%d] = {\n', w.ctype, k, total);

    data_flat = w.data(:);
    for j = 1:total
        if mod(j, 8) == 1
            fprintf(fid_c, '    ');
        end
        if strcmp(w.ctype, 'uint16_t')
            fprintf(fid_c, '%5u', data_flat(j));
        elseif strcmp(w.ctype, 'int16_t')
            fprintf(fid_c, '%6d', data_flat(j));
        elseif strcmp(w.ctype, 'uint32_t')
            fprintf(fid_c, '%10u', data_flat(j));
        else  % int32_t
            fprintf(fid_c, '%11d', data_flat(j));
        end
        if j < total
            fprintf(fid_c, ',');
        end
        if mod(j, 8) == 0
            fprintf(fid_c, '\n');
        end
    end
    if mod(total, 8) ~= 0
        fprintf(fid_c, '\n');
    end
    fprintf(fid_c, '};\n\n');
end
fclose(fid_c);
fprintf('Wrote ulunas_matlab_weights.c\n');

