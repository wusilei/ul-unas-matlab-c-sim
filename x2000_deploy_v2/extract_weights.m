fprintf(fid_h, '#endif /* ULUNAS_MATLAB_WEIGHTS_H */\n');
fclose(fid_h);
fprintf('Wrote ulunas_matlab_weights.h\n');

%% ====================================================================== %
%% 3. Generate C source file
%% ====================================================================== %
fid_c = fopen(fullfile(out_dir, 'ulunas_matlab_weights.c'), 'w');
