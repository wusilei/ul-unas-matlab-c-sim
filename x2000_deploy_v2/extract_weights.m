% Declare all weight arrays as extern
keys_list = keys(weight_map);
for i = 1:length(keys_list)
    k = keys_list{i};
    w = weight_map(k);
    fprintf(fid_h, '/* %s %s %s */\n', w.q_comment, w.ctype, w.dims);
    fprintf(fid_h, 'extern const %s %s[%d];\n\n', w.ctype, k, numel(w.data));
